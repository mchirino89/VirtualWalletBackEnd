public class user implements Serializable {

    private String ci;

    public user() {
    }

    public user(String ci) {
        this.ci = ci;
    }

    public String getCi() {
        return ci;
    }

    public void setCi(String ci) {
        this.ci = ci;
    }

    @Override
    public String toString() {
        return "CI: "+this.ci;
    }
}
